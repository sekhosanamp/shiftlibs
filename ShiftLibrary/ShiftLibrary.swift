import Foundation

class ShiftLibrary {

    let name = "ShiftLibrary"

    public func addInteger(a: Int, b: Int) -> Int {
        return a + b
    }

    public func substractInteger(a: Int, b: Int) -> Int {
        return a - b
    }

}
